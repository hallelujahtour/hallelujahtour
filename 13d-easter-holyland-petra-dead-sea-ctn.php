mengunjungi GEREJA GANTUNG (Hanging Church), GEREJA KELUARGA KUDUS, tempat di mana bayi Yesus dan kedua orang tuanya 
pernah tinggal dalam pengungsiannya ke Mesir (Mat 2 : 13). SYNAGOGE BEN EZRA, tempat bayi Musa ditemukan oleh putri Firaun 
(Kel 2:10). Serta mengunjungi PAPYRUS INSTITUTE. Malam hari bisa mengikuti optional NILE CRUISE DINNER (MP, MS & MM)
