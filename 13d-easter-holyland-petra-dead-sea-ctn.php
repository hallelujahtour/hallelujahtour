benteng Kota Oman yang dibangun pada abad 16 oleh Portugis melewati GEDUNG PARLEMEN. MUTRAH SOUQ merupakan pasar 
Traditional yang cukup unik menjual berbagai souvenir kerajinan khas Oman. Daerah ini juga dikelilingi CORNICHE sepanjang 3 km 
yang terkenal akan keindahan lautnya. Selesainya akan kembali ke hotel untuk makan siang kemudian akan diantar menuju Airport 
untuk terbang menuju CAIRO. Tiba akan diantar ke hotel untuk check in (MP, MS & MM)
