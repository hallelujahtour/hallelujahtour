TEMBOK RATAPAN, tempat suci bagi umat Yahudi. Dilanjutkan menuju BUKIT ZION mengunjungi RUANG PERJAMUAN TERAKHIR (Mat 
22: 14-23) Yesus bersama murid-muridNya sebelum Dia ditangkap. Di tempat ini juga Roh Kudus dicurahkan kepada murid-murid 
Yesus pada Hari Pentakosta. Dilanjutkan menuju GEREJA GALLICANTU atau GEREJA AYAM BERKOKOK (Mat 26: 69-75) yang dulunya
merupakan rumah Imam Kayafas. (MP, MS & MM)

