								G1372 SHANGHAI – HUAIHUA 0853 – 1523<br>
								G1377 SHANGHAI – HUAIHUA 0930 – 1702<br>
								</font>
								Setelah bersantap pagi di Shanghai, perjalanan Anda akan dilanjutkan menuju Huaihua dengan menggunakan Bullet Train. 
								Sesampainya di Huaihua kita akan makan malam terlebih dahulu kemudian dengan bus bertolak ke Fenghuang. 
								Malam ini Anda akan diajak untuk mengagumi kota Fenghuang di malam hari yang penuh dengan lampu-lampu yang 
								menambah keindahan kota kuno Fenghuang.
								</span>
								
								<p class="mb-10" style="border-bottom: 1px solid #666"></p>
								
								<ul class="details-tag mb-10">
                                    <li><a href="#"><i class="pe-7s-ribbon"></i>
									<span style="color:#666">
									D03 : FENGHUANG - ZHANGJIAJIE (BLD)
									</span>
									</a></li>
                                </ul>
								<p class="mb-10">
								<span style="color:#666">
								Pagi hari setelah makan pagi, kita akan menikmati keindahan negeri burung Phoenix atau the City of Light – Fenghuang. 
								Jalan-jalan di kota Fenghuang hampir seluruhnya berupa Flagstone Street atau terbuat dari susunan batu alam yang di 
								atur dengan rapi. Di kota yang telah dinobatkan sebagai warisan budaya UNESCO ini Anda akan di ajak mengunjungi 
								Shawan Diaojia House, bangunan hunian penduduk yang merupakan landmark unik di kota ini yang terletak 
								di tepi sungai Tuojiang. Kemudian melalui Tiao Yan, jembatan batu yang harus kita lalui dengan pijakan 
								satu-persatu untuk menuju North Gate. Hongqiao Bridge merupakan tujuan kita selanjutnya, 
								Jembatan yang terdiri atas dua lantai ini memiliki arsitektur unik. <br><br>
								
								Selanjutnya perjalanan di lanjutkan menuju Zhangjiajie yang sebagian orang mengartikannya sebagai “Jalan Menuju Surga”. 
								Di wilayah yang menginspirasi pembuatan filem Avatar ini, Anda akan diajak naik dengan menggunakan 
								Cable Car menuju Tian Men Shan. Kemudian di lanjutkan naik kembali dengan menggunakan elevator dan disana 
								Anda dapat menjajal anak tangga yang konon berjumlah 999 hingga menjapai puncak Tian Men Cave yang terkenal 
								dengan sebutan Stairway to Heaven. Bagi yang ingin menguji adrenalin Anda, silahkan mencoba melalui 
								The Glass Sky Walk, berjalan di salah satu pinggiran tebing dimana pijakan Anda terbuat dari bahan kaca.
								</span>
								</p>
								
								<p class="mb-10" style="border-bottom: 1px solid #666"></p>
