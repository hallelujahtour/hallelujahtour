								<p class="mb-10">
								<span style="color:#666">
								Hari ini seharian Anda akan dapat mengeksplor wilayah Zhangjiajie yang bila di lihat dari jauh gunung-gunung batu 
								yang menjulang itu terlihat seperti pilar menyangga langit. Salah satu puncaknya yang bernama Tianzishan akan kita 
								kunjungi hari ini. Tianzi Mountain adalah wilayah yang menurut banyak orang paling indah bila di bandingkan dengan 
								wilayah Zhangjiajie lainnya, karena memiliki gunung-gunung batu pilar terbanyak. Berlanjut menuju Helong Park, 
								taman yang memiliki patung perunggu Marshal Helong setinggi 6,5meter yang berada kira-kira 1KM di sisi kiri Tianzishan.<br><br> 
								
								Kemudian Anda akan di bawa naik lagi dengan menggukanan Bailong Elevator, yang merupakan elevator outdoor tertinggi 
								di dunia yang menjulang 326meter menuju Yunjiajie, dimana Anda dapat menyaksikan hampir seluruh wilayah Zhangjiajie 
								dari ketinggian. Lalu Anda turun kembali dengan menggunakan Cable Car.
								</span>
								</p>
								
								<p class="mb-10" style="border-bottom: 1px solid #666"></p>
								
