								<span style="color:#666">
								Pagi hari sebelum bertolak ke Changsha, kita akan mengunjungi Grand Canyon Glass Bridge yang merupakan jembatan panjang 
								dengan lantai kaca dan dengan desain arsitekrut yang menajubkan. Anda bisa menguji keberanian anda melintasi 
								jembatan kaca dengan pemandangan jurang yang sangat dalam dibawah nya.<br><br> 
								
								Selanjutnya perjalanan menuju ke Changsha dan langsung menuju Changsa International Financial Square. 
								Satu area tepat di jantung kota Changsa dengan dua gedung pencakar langitnya yang menjulang dan merupakan landmark 
								terbaru kota ini dan juga memiliki fasilitas komersial yang sangat lengkap.<br><br>
								
								Kemudian menuju Huangxing Road, pasar raya yang membujur dari Huangxing station hingga Nandamen, 
								dimana Anda dapat menikmati makanan tradisinal, souvenier hingga musik. Dan bagi yang suka wisata kuliner Pozijie 
								Food Street akan menjadi pilihan terbaik Anda, karena disana merupakan surganya Hunan Food dan Chinese 
								Specialties baik yang di sajikan dengan cara tradisional maupun moderen.
								</span>
								</p>
								
